    public VIEW(java.awt.Frame parent, boolean modal, String path) {
        super(parent, modal);
        
        initComponents();
        
        File a=new File(path);
        
        this.jLabel1.setText(a.getName());
        
        try
        {
            String text="";
            BufferedReader L = new BufferedReader(new java.io.FileReader(path));
            
            
                String S = null;
                while ((S = L.readLine()) != null) {
                    text+=S+"\n";
                }
                
                L.close();
                
                this.jTextArea1.setText(text);
        }
        catch(FileNotFoundException e)
        {
            JOptionPane.showMessageDialog(rootPane, "NO SE HA ENCONTRADO EL ARCHIVO","ERROR - FileNotFoundException",0);
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(rootPane, "NO HAY MEDIOS","ERROR - IOException",0);
        }
    }


