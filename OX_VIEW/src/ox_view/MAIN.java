private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    javax.swing.JFileChooser F1 = new javax.swing.JFileChooser();

    F1.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

    if (F1.showOpenDialog(null) == javax.swing.JFileChooser.APPROVE_OPTION) {
        a = F1.getSelectedFile();

        this.jLabel1.setText(a.getAbsolutePath());


        javax.swing.DefaultListModel Ma = new javax.swing.DefaultListModel();
        Ma.removeAllElements();


        String[] A = a.list();
        File[] FA = a.listFiles();

        for (int i = 0; i < A.length; i++) {
            Ma.addElement(A[i]);
        }

        this.jList3.setModel(Ma);
    } else {
        this.setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
    }


}//GEN-LAST:event_jButton1ActionPerformed

