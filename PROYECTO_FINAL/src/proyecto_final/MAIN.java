    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultListModel Ma = new DefaultListModel();

        String dir = jTextField1.getText().toUpperCase();


        if (dir.equals("C") | dir.equals("D") | dir.equals("E") | dir.equals("F") | dir.equals("G") | dir.equals("H")) {
            dir += ":\\";
        }


        a = new File(dir);

        if (a.exists()) {

            Ma.removeAllElements();
            String A[] = a.list();

            for (int i = 0; i < A.length; i++) {
                Ma.addElement(A[i]);
            }

            jTextField1.setText(a.getAbsolutePath());
            jList1.setModel(Ma);

        } else {
            JOptionPane.showMessageDialog(rootPane, "RUTA INVALIDA");
            jList1.setToolTipText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

