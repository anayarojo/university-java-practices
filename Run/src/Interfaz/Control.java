private void btn_oficinaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_oficinaMouseClicked
    this.setIconImage (new ImageIcon(getClass().getResource("/Iconos/Panel.png")).getImage());
    this.setTitle("CONTROL DE OFICINA");
    Layer.removeAll();
    btn_oficina.setBackground(new java.awt.Color(0,95,173)); //Color azul
    Paneles.PanelControl Pc=new Paneles.PanelControl(Layer.getSize(), fecha, Layer);
    Layer.add(Pc,BorderLayout.CENTER);
    Layer.repaint();
    
    oficina=1;registrar=0;busqueda=0;informes=0;registros=0;opciones=0;salir=0;
    MauseExit();
}//GEN-LAST:event_btn_oficinaMouseClicked

