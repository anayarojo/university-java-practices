private void btn_salirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_salirMouseClicked
    this.setIconImage (new ImageIcon(getClass().getResource("/Iconos/lock.png")).getImage());
    this.setTitle("CERRAR");
    Layer.removeAll();
    oficina=0;registrar=0;busqueda=0;informes=0;registros=0;opciones=0;salir=1;
    MauseExit();
    Menu M=new Menu();
    Dialogos.Opcion C=new Dialogos.Opcion(this, true);
    C.setTitle("Cerrar sesion");
    //C.setIconImage (new ImageIcon(getClass().getResource("/Recursos/lock.png")).getImage());
    C.show();
    
    int opcion=C.opcion();
    if(opcion==1)
    {
        M.show();
        this.dispose();
    }
    else
    {
        C.dispose();
        btn_oficinaMouseClicked(null);
        
    }
}//GEN-LAST:event_btn_salirMouseClicked


