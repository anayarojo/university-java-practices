private void btn_entrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_entrarMouseClicked
    String id="", pass="", db_id, db_nom = null, db_ape = null, db_pass = null;
    id=txtId.getText();
    pass=txtPassword.getText();
    
    if(id.equals(""))
    {
        E.setTitle("Exception");
        E.CambiarTexto("No has ingresado ID empleado ni Password  ");
        E.pack();E.setLocationRelativeTo(null);E.show();
    }
    else
    {
        Query="SELECT Id,Nombre,Apellido,Pass FROM empleados WHERE  Id="+id;
        Conect=C.Conectar(Datos, Pass, Dir);
            try 
            {
                St=Conect.createStatement();
                Rs=St.executeQuery(Query);
            
                while(Rs.next())
                {
                    db_id=Rs.getString("Id");
                    db_nom=Rs.getString("Nombre");
                    db_ape=Rs.getString("Apellido");
                    db_pass=Rs.getString("Pass");
                }     
            } 
            catch (SQLException e) 
            {
                E.setTitle("SQLException");
                E.CambiarTexto("Error "+e+"  ");
                E.pack();E.setLocationRelativeTo(null);E.show();
            }
            catch (Exception e) 
            {
                E.setTitle("Exception");
                E.CambiarTexto("Error "+e+"  ");
                E.pack();E.setLocationRelativeTo(null);E.show();
            }
        
            if(pass.equals(db_pass))
            {
                M.setTitle("Bienvenido");
                M.CambiarTexto("Hola "+db_nom+" "+db_ape+", Usted ha ingresado en el sistema  ");
                M.pack();M.setLocationRelativeTo(null);M.show();
                this.dispose();
            }
            else if(db_pass==null)
            {
                E.setTitle("Login");
                E.CambiarTexto("La ID empleado que ingreso no es valida  ");
                E.pack();E.setLocationRelativeTo(null);E.show();
                txtId.setText("");
                txtPassword.setText("");
                txtId.requestFocus();
            }
            else
            {
                E.setTitle("Login");
                E.CambiarTexto("Contrase√±a incorrecta  ");
                E.pack();E.setLocationRelativeTo(null);E.show();
                txtPassword.setText("");
                txtPassword.requestFocus();
            }
    }
}//GEN-LAST:event_btn_entrarMouseClicked

